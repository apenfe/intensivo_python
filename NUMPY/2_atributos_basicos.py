import numpy as np

a = np.array([[12,25,3],[7,14,92]])
b = np.array([5.1,4.3,8.2])

print(a)
print(b)

print(a.dtype) # indica el tipo de dato almacenado
print(b.dtype) # indica el tipo de dato almacenado

print(a.ndim) # indica dimensionalidad del arreglo
print(b.ndim) # indica dimensionalidad del arreglo

print(a.shape) # indica la cantidad de datos en cada dimension
print(b.shape) # indica la cantidad de datos en cada dimension

print(a.size) # indica numero total de elementos
print(b.size) # indica numero total de elementos